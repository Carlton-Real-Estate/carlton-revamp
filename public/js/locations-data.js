// Get current language (default to English)
function getCurrentLanguage() {
    return document.documentElement.lang || 'en';
}

// Populate location dropdown based on current language
function populateLocationDropdown(selectElement) {
    if (!selectElement) return;
    
    const lang = getCurrentLanguage();
    const locations = locationsData[lang] || locationsData.en;
    
    // Clear existing options except "All Areas"
    const firstOption = selectElement.querySelector('option:first-child');
    selectElement.innerHTML = '';
    
    // Re-add first option
    if (firstOption) {
        selectElement.appendChild(firstOption);
    }
    
    // Add all locations
    locations.forEach(location => {
        const option = document.createElement('option');
        option.value = location.value;
        option.textContent = location.name;
        selectElement.appendChild(option);
    });
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', function() {
    const locationSelects = document.querySelectorAll('#location-filter, #map-location');
    locationSelects.forEach(select => {
        populateLocationDropdown(select);
    });
});

// Export for use in other scripts
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { locationsData, populateLocationDropdown };
}
